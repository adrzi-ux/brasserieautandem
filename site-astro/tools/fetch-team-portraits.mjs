#!/usr/bin/env node
// Télécharge les photos portraits de l'équipe depuis Pexels

import fs from 'node:fs';
import path from 'node:path';
import https from 'node:https';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const API_KEY = 'Ps9vdBhiyY8EbkV3osK4DuFgFa46eKH5laemzsH7J3E0JD9EZ66mZHaR';

const PORTRAITS = {
  'camille-aubert': 'female chef kitchen portrait',
  'leo-pinault':    'male waiter bistro portrait',
  'ines-cabrol':    'female sommelier wine portrait',
};

const OUT_DIR = path.join(ROOT, 'public/images/team');
fs.mkdirSync(OUT_DIR, { recursive: true });

function fetchJson(url) {
  return new Promise((resolve, reject) => {
    https.get(url, { headers: { Authorization: API_KEY } }, res => {
      let data = '';
      res.on('data', c => data += c);
      res.on('end', () => resolve(JSON.parse(data)));
    }).on('error', reject);
  });
}

function download(url, dest) {
  return new Promise((resolve, reject) => {
    const file = fs.createWriteStream(dest);
    https.get(url, res => {
      if (res.statusCode === 301 || res.statusCode === 302) {
        return download(res.headers.location, dest).then(resolve).catch(reject);
      }
      res.pipe(file);
      file.on('finish', () => file.close(resolve));
    }).on('error', reject);
  });
}

const results = {};

for (const [key, query] of Object.entries(PORTRAITS)) {
  const data = await fetchJson(
    `https://api.pexels.com/v1/search?query=${encodeURIComponent(query)}&per_page=3&orientation=portrait`
  );
  const photo = data.photos?.[0];
  if (!photo) { console.log(`❌ ${key}: no result`); continue; }

  const url = photo.src.large;
  const dest = path.join(OUT_DIR, `${key}.jpg`);
  await download(url, dest);

  results[key] = {
    src: `/images/team/${key}.jpg`,
    alt: photo.alt || query,
    photographer: photo.photographer,
    photographerUrl: photo.photographer_url,
    pexelsUrl: photo.url,
  };
  console.log(`✓ ${key} — ${photo.photographer}`);
}

// Write team-images.ts
const ts = `// AUTO-GENERATED by tools/fetch-team-portraits.mjs — DO NOT EDIT BY HAND

export type ImageData = {
  src: string; alt: string; photographer: string;
  photographerUrl: string; pexelsUrl: string;
};

export const TEAM_IMAGES: Record<string, ImageData> = ${JSON.stringify(results, null, 2)};
`;

fs.writeFileSync(path.join(ROOT, 'src/data/team-images.ts'), ts);
console.log('✓ src/data/team-images.ts written');
